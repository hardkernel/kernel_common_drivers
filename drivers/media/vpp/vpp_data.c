static void _write_reg_by_mask(unsigned int addr, unsigned int mask,
	unsigned int val, enum io_mode_e mode)
{
	unsigned int i = 0;
	unsigned int data = 0;
	unsigned int start = 0;
	unsigned int len = 0;
	unsigned int update_flag = 0;
	unsigned int match_flag = 0;

	if (!mask || !addr)
		return;

//	pr_vpp(PR_DEBUG_DATA, "[%s] addr:0x%x, val:0x%x, mask:0x%x\n",
//		__func__, addr, val, mask);
//	pr_vpp(PR_DEBUG_DATA, "[%s] mode:%d\n",
//		__func__, mode);

	for (i = 0; i < 32; i++) {
		if (!match_flag &&
			(mask & (1 << i))) {
			start = i;
			match_flag = 1;
//			pr_vpp(PR_DEBUG_DATA, "[%s] start:%d, match_flag:%d\n",
//				__func__, start, match_flag);
		}

		if (match_flag) {
			if ((mask & (1 << i)) == 0) {
				len = i - start;
				match_flag = 0;
				update_flag = 1;
//				pr_vpp(PR_DEBUG_DATA, "[%s] start:%d, len:%d\n",
//					__func__, start, len);
			} else if (i == 31) {
				len = 32 - start;
				match_flag = 0;
				update_flag = 1;
//				pr_vpp(PR_DEBUG_DATA, "[%s] 31 start:%d, len:%d\n",
//					__func__, start, len);
			}
		}

		if (update_flag && len) {
			data = vpp_mask_int(val, start, len);
			WRITE_VPP_REG_BITS_BY_MODE(mode, addr,
				data, start, len);

//			pr_vpp(PR_DEBUG_DATA, "[%s] addr/data:0x%x/0x%x, start/len:%d/%d\n",
//				__func__, addr, data, start, len);

			update_flag = 0;
		}
	}
}

static void _update_data_page_reg(struct vpp_pq_tuning_page_s *data)
{
	int i, j;
	int reg_count = 0;
	unsigned char start = 0;
	unsigned char len = 0;
	unsigned int tmp = 0;
	struct vpp_pq_tuning_reg_s *reg_list;
	struct vpp_pq_tuning_reg_s *cur_reg;
	struct vpp_pq_page_s *cur_page;

	if (!data)
		return;

	if (!data->reg_list_ptr || data->reg_count == 0)
		return;

	reg_list = data->reg_list_ptr;

	for (i = 0; i < EN_PAGE_MODULE_MAX; i++) {
		if (pq_table[i].page_addr == data->page_addr) {
			cur_page = &pq_table[i].page[0];
			reg_count = pq_table[i].count;
			break;
		}
	}

	for (i = 0; i < data->reg_count; i++) {
		cur_reg = reg_list + i;
		for (j = 0; j < reg_count; j++) {
			if (cur_page->reg[j].addr == cur_reg->reg_addr) {
				start = cur_reg->bit_start;
				len = cur_reg->bit_end - cur_reg->bit_start + 1;
				tmp = cur_page->reg[j].val;
				tmp = vpp_insert_int(tmp, cur_reg->val, start, len);
				if (cur_page->reg[j].val != tmp) {
					cur_page->reg[j].val = tmp;
					cur_page->reg[j].update = 1;
				}
				break;
			}
		}
	}
}

static void _write_data_table(enum vpp_page_module_e module,
	int index)
{
	int i = 0;
	int val = 0;
	int mask_index = 0;
	unsigned int addr = 0;

	if (module > EN_PAGE_MODULE_MAX - 1 ||
		index > PAGE_TBL_COUNT_MAX - 1)
		return;

	for (i = 0; i < pq_table[module].count; i++) {
		if (!pq_table[module].page[index].reg[i].update)
			continue;

		addr = ADDR_PARAM(pq_table[module].page_addr,
			pq_table[module].page[index].reg[i].addr);
		val = pq_table[module].page[index].reg[i].val;
		mask_index = pq_table[module].page[index].reg[i].mask_type;

		if (mask_list[mask_index] == 0xffffffff) {
			WRITE_VPP_REG_BY_MODE(EN_MODE_RDMA, addr, val);
		} else {
			val &= mask_list[mask_index];
			_write_reg_by_mask(addr, mask_list[mask_index],
				val, EN_MODE_RDMA);
		}

		pq_table[module].page[index].reg[i].update = 0;
	}
}

