	{
		$out_msg = <<END;
\$ git reset HEAD^ | ./scripts/amlogic/merge_check.pl
total $err_cnt errors.
${err_msg}

END

		$exit = 1;
		print $out_msg;
	}
	else
	{
		print "";
	}
}
